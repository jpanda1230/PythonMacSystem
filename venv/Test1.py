driver.find_element_by_id("email").send_keys("test@gmail.com")
time.sleep(1)
driver.find_element_by_id("telefone").clear()
driver.find_element_by_id("telefone").send_keys("11111111111")
time.sleep(1)
driver.find_element_by_id("btcon").click()
time.sleep(2)
